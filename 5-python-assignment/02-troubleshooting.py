zoo = ['Lion', 'Zebra', 'Giraffe', 'Hippo'] 
for animal in zoo:
    if animal == "Lion":
        print("Alex the " + animal)
    elif animal == 'Zebra':
        print ("Marty the " + animal)    
    elif animal == 'Giraffe':
        print ("Melman the " + animal)
    elif animal == 'Hippo':
        print ("Gloria the " + animal)